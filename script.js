// Variables

let questions=[];

let current=0;

let answers=[];

let timer;

let timeLeft=0;




// START TEST

function startTest(){


let subject=
document.getElementById("subject").value;


let count=
Number(
document.getElementById("questionCount").value
);


let minutes=
Number(
document.getElementById("timeInput").value
);



if(subject=="combined"){


questions=[...questionBank];


}

else{


questions=
questionBank.filter(q=>
q.subject==subject
);


}



questions.sort(
()=>Math.random()-0.5
);



questions=
questions.slice(0,count);



answers=
new Array(questions.length)
.fill(null);



document.getElementById("total")
.innerHTML=
questions.length;



timeLeft=minutes*60;


startTimer();


createPalette();


loadQuestion();


}




// TIMER

function startTimer(){


clearInterval(timer);


timer=setInterval(()=>{


let m=Math.floor(timeLeft/60);

let s=timeLeft%60;



document.getElementById("timer")
.innerHTML=

`${m.toString().padStart(2,"0")}:
${s.toString().padStart(2,"0")}`;



if(timeLeft<=0){

clearInterval(timer);

submitTest();

}


timeLeft--;



},1000);


}






// LOAD QUESTION


function loadQuestion(){


let q=questions[current];


document.getElementById("questionNumber")
.innerHTML=
"Question "+(current+1);



document.getElementById("questionText")
.innerHTML=
q.question;



let box=
document.getElementById("options");


box.innerHTML="";



q.options.forEach((op,i)=>{


let btn=
document.createElement("button");


btn.className="option";


let letter=
String.fromCharCode(65+i);



btn.innerHTML=
letter+". "+op;



btn.onclick=function(){


answers[current]=letter;


loadQuestion();


};



if(answers[current]==letter){

btn.classList.add("selected");

}



box.appendChild(btn);



});



updatePalette();


updateAnswered();


}







// PALETTE


function createPalette(){


let p=
document.getElementById("palette");


p.innerHTML="";



questions.forEach((q,i)=>{


let btn=
document.createElement("button");


btn.className="paletteBtn";


btn.innerHTML=i+1;



btn.onclick=function(){

current=i;

loadQuestion();

};



p.appendChild(btn);


});

}


function updatePalette(){


let buttons=
document.querySelectorAll(".paletteBtn");


buttons.forEach((b,i)=>{


b.className="paletteBtn";


if(answers[i])

b.classList.add("answered");

else

b.classList.add("notAnswered");



if(i==current)

b.classList.add("current");


});


}




// NAVIGATION


function nextQuestion(){


if(current<questions.length-1){

current++;

loadQuestion();

}

}



function previousQuestion(){


if(current>0){

current--;

loadQuestion();

}

}



function saveNext(){

nextQuestion();

}



function clearAnswer(){

answers[current]=null;

loadQuestion();

}



function reviewQuestion(){

nextQuestion();

}




function updateAnswered(){

document.getElementById("answered")
.innerHTML=
answers.filter(x=>x).length;

}





// SUBMIT


function submitTest(){


clearInterval(timer);


let score=0;


let correct=0;

let wrong=0;



questions.forEach((q,i)=>{


if(answers[i]==q.answer){

score+=4;

correct++;

}

else if(answers[i]){

score-=1;

wrong++;

}


});



document.querySelector(".container")
.style.display="none";



document.getElementById("result")
.classList.remove("hidden");



document.getElementById("score")
.innerHTML=
score;



document.getElementById("analysis")
.innerHTML=

`
Correct: ${correct}<br>
Wrong: ${wrong}<br>
Unattempted: ${questions.length-correct-wrong}
`;

}
